public class MaxNumber {

	public static void main(String[] args) {
		
		int a = 50 , b = 20 , c = 150;
		
		if(a>b && a>c)
		{
			System.out.println("Max of three number is a = "+a);
		}
		else if(b>c)
		{
			System.out.println("Max of three number is b = "+b);
		}
		
		else
		{
			System.out.println("Max of three number is c = "+c);
		}

	}

}